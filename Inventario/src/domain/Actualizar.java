    public Actualizar() {
       initComponents();
        ConexionBD bD = new ConexionBD("compu");
        List<ComputadoraBean> list = bD.consultar("SELECT * FROM tecno");
        if(list != null){
            for (int i =0; i<list.size(); i++){
                ComputadoraBean bean = list.get(i);
                 datatable.getModel().setValueAt(bean.getNoSerie(),i, 0);
                 datatable.getModel().setValueAt(bean.getMarca(),i, 1);
                 datatable.getModel().setValueAt(bean.getModelo(),i, 2);
                 datatable.getModel().setValueAt(bean.getDisco(),i, 3);
                 datatable.getModel().setValueAt(bean.getRam(),i, 4);
                 datatable.getModel().setValueAt(bean.getProcesador(),i, 5);
                 datatable.getModel().setValueAt(bean.getEstado(),i, 6);
            }
                
        }
      datatable.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
           @Override
           public void valueChanged(ListSelectionEvent e) {
               txtNoSerie.setText((String)datatable.getValueAt(datatable.getSelectedRow(), 0));
               txtMarca.setText((String)datatable.getValueAt(datatable.getSelectedRow(), 1));
               txtModelo.setText((String)datatable.getValueAt(datatable.getSelectedRow(), 2));
               txtDisco.setText(String.valueOf((Integer)datatable.getValueAt(datatable.getSelectedRow(), 3)));
               txtRam.setText(String.valueOf((Integer)datatable.getValueAt(datatable.getSelectedRow(), 4)));
               txtProcesador.setText((String)datatable.getValueAt(datatable.getSelectedRow(), 5));
               cmbestado.getModel().setSelectedItem((String)datatable.getValueAt(datatable.getSelectedRow(), 6));
           }
       });
    }

