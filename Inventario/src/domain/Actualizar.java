    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        String serie; //Se empieza declarando las variables.
        String marca;
        String modelo;
        String disco;
        String ram;
        String procesador;
        String enunciadoSQL;
        
        
        serie=txtNoSerie.getText(); //Despues se obtiene el texto 
        marca=txtMarca.getText();
        modelo=txtModelo.getText().toUpperCase().trim();
        disco=txtDisco.getText().toUpperCase().trim();
        ram=txtRam.getText().toUpperCase().trim();
        procesador=txtProcesador.getText().toUpperCase().trim();
        
        //Aqui se coloca el enunciado sql para las acciones en la BD.
        enunciadoSQL="UPDATE  tecno SET noSerie='"+serie+"', marca='"+marca+"', "
                + "modelo='"+modelo+"', disco="+disco+", "
                + "ram="+ram+", procesador='"+procesador+"' WHERE noSerie="+serie;
        con.actualizar(enunciadoSQL); //Se realiza la acción.
    }//GEN-LAST:event_btnActualizarActionPerformed

