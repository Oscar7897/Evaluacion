    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        // TODO add your handling code here:
        /**
        * Inicia el tiempo.
        */
        Random r1 = new Random();
        int x;
        if (tipo == 0) {
            if (!suspendido) {
                hilo.start();
                suspendido = true;
            } else {
                hilo.resume();
            }
            for (int i = 1; i < 100; i++) {
                x = r1.nextInt(16);
                seleccionar(x);
            }
        } else if (tipo == 1) {
            for (int i = 1; i <= 1000; i++) {
                x = r1.nextInt(16);
                mover(x);
            }
            if (!suspendido) {
                hilo.start();
                suspendido = true;
            } else {
                hilo.resume();
            }
        }
        banInicio = true;
    }//GEN-LAST:event_btnIniciarActionPerformed

     private boolean esValidoMov(int pos) {
        boolean salida = false;
        int dx;//fila disponible
        int dy;//
        int posx;
        int posy;
        dx = disponible / 4;
        dy = disponible % 4;
        posx = pos / 4;
        posy = pos % 4;
        if (dx == posx) {
            if (Math.abs(disponible - pos) == 1) {
                salida = true;
            }
        }
        if (dy == posy) {
            if (Math.abs(disponible - pos) == 4) {
                salida = true;
            }
        }
        return salida;
    }

    private void seleccionar(int pos) {

        if (!banClick) { // Primer clic
            pos1 = pos;
            tablero[pos1].setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0), 2));
            banClick = true;
        } else {
            pos2 = pos;
            tablero[pos1].setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0), 2));
            banClick = false;
            //Intercambia imagenes de las posiciones pos1 y pos2
            intercambiar(pos1, pos2);
        }
    }

    private void intercambiar(int pos1, int pos2) {
        JLabel lblTemp = new JLabel();
        lblTemp.setIcon(tablero[pos1].getIcon());
        tablero[pos1].setIcon(tablero[pos2].getIcon());
        tablero[pos2].setIcon(lblTemp.getIcon());
        int tempo;
        tempo = datos[pos1];
        datos[pos1] = datos[pos2];
        datos[pos2] = tempo;
    }

    private void revisar() {
        if (banInicio && !banClick && verificar()) {
            JOptionPane.showMessageDialog(this, "Ya ganaste! ");
            hilo.suspend();
            suspendido = true;
            // String tiempo=txtTiempos.getText();
            String nuevo = con + ".     " + lbltiempo.getText();
            // txtTiempos.setText(nuevo+"\n"+tiempo);
            con++;
            //banPlay=false;
            cmbTipo.setSelectedIndex(0);
            tipo = 0;
            this.cmbImagenesActionPerformed(null);
            for (int i = 0; i < 16; i++) {
                tablero[i].setBorder(null);
            }
            banInicio = false;      //<--------------
        }
    }

    private void moverLinea(int pos) {
        if (banInicio) {
//            if (tipo==2) {
//                lblTablero.setIcon(tablero[0].getIcon());
//            }
        }
        if (pos / 4 == disponible / 4) {
            if (pos > disponible) {
                for (int i = disponible + 1; i <= pos; i++) {
                    mover(i);
                }
            } else {
                for (int i = disponible - 1; i >= pos; i--) {
                    mover(i);
                }
            }
        } else if (pos % 4 == disponible % 4) {
            if (pos > disponible) {
                for (int i = disponible + 4; i <= pos; i = i + 4) {
                    mover(i);
                }
            } else {
                for (int i = disponible - 4; i >= pos; i = i - 4) {
                    mover(i);
                }
            }
        }
//        if (tipo==2) {
//            tablero[12].setIcon(lblTempo);
//        }
    }

    /**
     * verificar si ya ganÃ³, es decir si el rompecabezas ya esta armado
     * correctamente.
     *
     * @return true= ya ganÃ³; false = no ganÃ³
     */
    private boolean verificar() {
        for (int i = 0; i < 16; i++) {
            if (datos[i] != i) {
                return false;
            }
        }
        return true;
    }

    private void mover(int pos) {
        if (banInicio) {
            if (tipo == 0) {
                seleccionar(pos);
            } else if (tipo == 1) {
                if (esValidoMov(pos)) {
                    intercambiar(pos, disponible);
                    disponible = pos;
                }
            }
        }
    }

    public void tirar(int pos) {
        if (tipo == 0) {
            mover(pos);
        } else if (tipo == 1) {
            moverLinea(pos);
        }
        revisar();
    }
    Thread hilo = new Thread() {
        public void run() {
            try {
                while (true) {
                    if (deci > 99) {
                        deci = 0;
                        seg++;
                    }
                    if (seg > 59) {
                        seg = 0;
                        min++;
                    }
                    if (min > 59) {
                        min = 0;
                        hora++;
                    }
                    String s = "", m = "", h = "", d = "";
                    if (seg < 10) {
                        s = "0" + seg;
                    } else {
                        s = "" + seg;
                    }
                    if (min < 10) {
                        m = "0" + min;
                    } else {
                        m = "" + min;
                    }
                    if (hora < 10) {
                        h = "0" + hora;
                    } else {
                        h = "" + hora;
                    }
                    if (deci < 10) {
                        d = "0" + deci;
                    } else {
                        d = "" + deci;
                    }
                    deci++;
                    hilo.sleep(9);
                    lbltiempo.setText(h + ":" + m + ":" + s + ":" + d);
                }
            } catch (InterruptedException ie) {
                JOptionPane.showMessageDialog(rootPane, ie.getMessage());
            }
        }
    };

